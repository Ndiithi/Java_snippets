package com.JsonServlet.model;

/**
 *
 * @author duncan
 */
public class Person {
    String name;
    String age;
    String gender;
    String primary_sch;
    String secondary_sch;
    String college;

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public String getAge() {
        return age;
    }

    public void setAge(String age) {
        this.age = age;
    }

    public String getGender() {
        return gender;
    }

    public void setGender(String gender) {
        this.gender = gender;
    }

    public String getPrimary_sch() {
        return primary_sch;
    }

    public void setPrimary_sch(String primary_sch) {
        this.primary_sch = primary_sch;
    }

    public String getSecondary_sch() {
        return secondary_sch;
    }

    public void setSecondary_sch(String secondary_sch) {
        this.secondary_sch = secondary_sch;
    }

    public String getCollege() {
        return college;
    }

    public void setCollege(String college) {
        this.college = college;
    }
    
    
}
